public class StringExercise {
    public static void main(String[] args) {

//        String Basics.

//        Create a class named StringExercise with a main method.
//
//        For each of the following output examples: create a String variable that
//        contains the desired output and print it out to the console, you can do
//        this with only one String variable and one print statement for each output example.

        String education = "We don't need no education";
        System.out.println(education);
        String control = "We don't need no thought control";
        System.out.println(control);

        String checkTO = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkTO);

        String window = "In windows, the main drive is usually C:\\";
        System.out.println(window);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                             "and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
